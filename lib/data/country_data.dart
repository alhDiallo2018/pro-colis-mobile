/// Returns countries filtered by query, sorted by name alphabetically
List<CountryInfo> searchCountries(String query) {
  if (query.isEmpty) return allCountries;
  final lower = query.toLowerCase();
  return allCountries.where((c) {
    return c.name.toLowerCase().contains(lower) ||
        c.dialCode.contains(lower) ||
        c.code.toLowerCase().contains(lower);
  }).toList();
}
